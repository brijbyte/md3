// Generates src/styles/color-themes.css from the seed colors below using the
// real MD3 HCT/tonal-palette algorithm (material-color-utilities), baked to
// static CSS once — no client-side computation. Re-run after changing seeds:
//   pnpm --filter docs build:color-themes
//
// Structure mirrors the library tokens.css: each theme emits its own reference
// tonal palette (--md-ref-palette-*), then sys colors reference those ref tones.
// The role→tone mapping is read from the library's tokens.json so docs themes
// stay in lockstep with the baseline sys→ref mapping.
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { argbFromHex, hexFromArgb, Hct, SchemeTonalSpot } from "@material/material-color-utilities";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outPath = join(root, "src/styles/color-themes.css");
const tokensPath = join(root, "../../packages/react/tokens/tokens.json");

const tokens = JSON.parse(readFileSync(tokensPath, "utf8"));
const refTones = Object.keys(tokens.ref.palette); // e.g. "primary40", "neutral-variant50"
const sysLight = tokens.color.light; // { role: refToneName }
const sysDark = tokens.color.dark;

// Excludes "default" (the library's baseline purple, #6750A4) — that theme
// needs no override CSS since it's already what tokens.css ships.
const SEEDS = {
  blue: "#1B6EF3",
  teal: "#00696B",
  green: "#3E6837",
  amber: "#8C5000",
  red: "#904A42",
  pink: "#984061",
};

// Map a tonal-palette name to the scheme's palette instance.
const paletteFor = (scheme, name) =>
  ({
    primary: scheme.primaryPalette,
    secondary: scheme.secondaryPalette,
    tertiary: scheme.tertiaryPalette,
    neutral: scheme.neutralPalette,
    "neutral-variant": scheme.neutralVariantPalette,
    error: scheme.errorPalette,
  })[name];

// "neutral-variant50" -> { name: "neutral-variant", tone: 50 }
const parseTone = (key) => {
  const [, name, tone] = key.match(/^([a-z-]+?)(\d+)$/);
  return { name, tone: Number(tone) };
};

// Reference palette (theme's own tones); identical for light/dark, emit once.
const refPaletteVars = (scheme) =>
  refTones
    .map((key) => {
      const { name, tone } = parseTone(key);
      return `    --md-ref-palette-${key}: ${hexFromArgb(paletteFor(scheme, name).tone(tone))};`;
    })
    .join("\n");

// Sys colors point at ref tones — same mapping across every theme.
const sysVars = (map) =>
  Object.entries(map)
    .map(([role, tone]) => `    --md-sys-color-${role}: var(--md-ref-palette-${tone});`)
    .join("\n");

const blocks = Object.entries(SEEDS).map(([name, seedHex]) => {
  const scheme = new SchemeTonalSpot(Hct.fromInt(argbFromHex(seedHex)), false, 0);
  // :root-prefixed to out-specify tokens.css (:root / :root:not([data-theme='light'])):
  // demos import tokens.css as JS, so a copy can land after this file in source order.
  return `  :root[data-color-theme='${name}'] {
${refPaletteVars(scheme)}
${sysVars(sysLight)}
  }

  :root[data-theme='dark'][data-color-theme='${name}'] {
${sysVars(sysDark)}
  }`;
});

const css = `/* Generated by scripts/build-color-themes.mjs — do not edit.
   Seed colors are hand-picked; palettes are the real MD3 tonal-palette
   algorithm output, baked to static CSS (no runtime color computation).
   Each theme sets its own --md-ref-palette-* tones; sys colors reference them. */
@layer theme {
${blocks.join("\n\n")}
}
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, css);
console.log(`Wrote ${outPath}`);
