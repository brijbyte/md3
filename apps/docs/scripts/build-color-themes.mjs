// Generates src/styles/color-themes.css from the seed colors below using the
// real MD3 HCT/tonal-palette algorithm (material-color-utilities), baked to
// static CSS once — no client-side computation. Re-run after changing seeds:
//   pnpm --filter docs build:color-themes
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import {
  argbFromHex,
  hexFromArgb,
  Hct,
  MaterialDynamicColors,
  SchemeTonalSpot,
} from "@material/material-color-utilities";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outPath = join(root, "src/styles/color-themes.css");

// Excludes "default" (the library's baseline purple, #6750A4) — that theme
// needs no override CSS since it's already what tokens.css ships.
const SEEDS = {
  blue: "#1B6EF3",
  teal: "#00696B",
  green: "#3E6837",
  amber: "#8C5000",
  red: "#904A42",
  pink: "#984061",
};

// Role order/names mirror packages/react/tokens/tokens.json's color.light keys.
const ROLES = [
  "primary",
  "onPrimary",
  "primaryContainer",
  "onPrimaryContainer",
  "secondary",
  "onSecondary",
  "secondaryContainer",
  "onSecondaryContainer",
  "tertiary",
  "onTertiary",
  "tertiaryContainer",
  "onTertiaryContainer",
  "error",
  "onError",
  "errorContainer",
  "onErrorContainer",
  "surface",
  "onSurface",
  "surfaceVariant",
  "onSurfaceVariant",
  "surfaceDim",
  "surfaceBright",
  "surfaceContainerLowest",
  "surfaceContainerLow",
  "surfaceContainer",
  "surfaceContainerHigh",
  "surfaceContainerHighest",
  "background",
  "onBackground",
  "outline",
  "outlineVariant",
  "shadow",
  "scrim",
  "surfaceTint",
  "inverseSurface",
  "inverseOnSurface",
  "inversePrimary",
];

const kebab = (name) => name.replace(/[A-Z]/g, (c) => `-${c.toLowerCase()}`);

const dynamicColors = new MaterialDynamicColors();

function schemeVars(seedHex, isDark) {
  const scheme = new SchemeTonalSpot(Hct.fromInt(argbFromHex(seedHex)), isDark, 0);
  return ROLES.map((role) => {
    const argb = dynamicColors[role]().getArgb(scheme);
    return `    --md-sys-color-${kebab(role)}: ${hexFromArgb(argb)};`;
  }).join("\n");
}

const blocks = Object.entries(SEEDS).map(
  ([name, seedHex]) => `  [data-color-theme='${name}'] {
${schemeVars(seedHex, false)}
  }

  [data-theme='dark'][data-color-theme='${name}'] {
${schemeVars(seedHex, true)}
  }`,
);

const css = `/* Generated by scripts/build-color-themes.mjs — do not edit.
   Seed colors are hand-picked; palettes are the real MD3 tonal-palette
   algorithm output, baked to static CSS (no runtime color computation). */
@layer theme {
${blocks.join("\n\n")}
}
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, css);
console.log(`Wrote ${outPath}`);
