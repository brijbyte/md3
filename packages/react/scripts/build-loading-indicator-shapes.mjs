// Precomputes MD3 Loading Indicator morph keyframes into
// src/generated/loading-indicator-shapes.ts. The hard part (RoundedPolygon
// construction + Morph's vertex-matching algorithm, ported from
// androidx.graphics.shapes — see scripts/loading-indicator/) runs here, once,
// at build time. The component only does cheap per-frame lerp + rotation.
// Imported by vite.config.ts (runs on build + watch); also runnable as a CLI.
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { normalizePolygon } from "./loading-indicator/geometry.mjs";
import { morphMatch } from "./loading-indicator/morph.mjs";
import {
  ACTIVE_INDICATOR_SCALE,
  DETERMINATE_SEQUENCE,
  INDETERMINATE_SEQUENCE,
  scaleFactorForSequence,
} from "./loading-indicator/shapes.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
export const loadingIndicatorShapesPath = join(root, "src/generated/loading-indicator-shapes.ts");

// Output viewBox is 100x100, shape centered at (50,50) — the component draws
// this directly with no further scaling; only rotation is applied live (CSS).
const VIEWBOX = 100;

function bakeSequence(sequence, circular) {
  const scaleFactor = scaleFactorForSequence(sequence) * ACTIVE_INDICATOR_SCALE;
  const bake = (x, y) => [
    50 + (x - 0.5) * VIEWBOX * scaleFactor,
    50 + (y - 0.5) * VIEWBOX * scaleFactor,
  ];
  const bakeCubic = (c) => {
    const out = Array.from({ length: 8 });
    for (let i = 0; i < 8; i += 2) {
      const [x, y] = bake(c[i], c[i + 1]);
      out[i] = Math.round(x * 1000) / 1000;
      out[i + 1] = Math.round(y * 1000) / 1000;
    }
    return out;
  };

  const normalized = sequence.map((p) => normalizePolygon(p));
  const morphs = [];
  for (let i = 0; i < normalized.length; i++) {
    if (i + 1 < normalized.length) morphs.push(morphMatch(normalized[i], normalized[i + 1]));
    else if (circular) morphs.push(morphMatch(normalized[i], normalized[0]));
  }
  return morphs.map((pairs) => pairs.map(([c1, c2]) => [bakeCubic(c1), bakeCubic(c2)]));
}

const morphTs = (morph) =>
  `[\n${morph.map(([c1, c2]) => `    [[${c1.join(", ")}], [${c2.join(", ")}]],`).join("\n")}\n  ]`;

export function buildLoadingIndicatorShapes() {
  const indeterminate = bakeSequence(INDETERMINATE_SEQUENCE(), true);
  const determinate = bakeSequence(DETERMINATE_SEQUENCE(), false);

  const contents = `// Generated by scripts/build-loading-indicator-shapes.mjs. Do not edit by hand.
// Each Morph is a list of [cubicStart, cubicEnd] pairs (8-number Bezier
// arrays: [a0x,a0y,c0x,c0y,c1x,c1y,a1x,a1y]) in a 0-100 viewBox, already
// matched in count/order so lerp(cubicStart, cubicEnd, progress) is valid.
export type Cubic = readonly number[];
export type MorphPair = readonly [Cubic, Cubic];
export type Morph = readonly MorphPair[];

// SoftBurst -> Cookie9Sided -> Pentagon -> Pill -> Sunny -> Cookie4Sided -> Oval -> (loops to SoftBurst)
export const INDETERMINATE_MORPHS: readonly Morph[] = [
${indeterminate.map((m) => `  ${morphTs(m)},`).join("\n")}
];

// Circle (18°-rotated) -> SoftBurst
export const DETERMINATE_MORPH: Morph = ${morphTs(determinate[0])};

export const MORPH_INTERVAL_MS = 650;
export const GLOBAL_ROTATION_DURATION_MS = 4666;
export const VIEWBOX_SIZE = ${VIEWBOX};
`;

  mkdirSync(dirname(loadingIndicatorShapesPath), { recursive: true });
  writeFileSync(loadingIndicatorShapesPath, contents);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  buildLoadingIndicatorShapes();
  console.log(`Wrote ${loadingIndicatorShapesPath}`);
}
