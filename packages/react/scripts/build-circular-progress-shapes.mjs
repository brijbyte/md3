// Precomputes the wavy CircularProgress's circle<->star Morph into
// src/generated/circular-progress-shapes.ts. Reuses the same RoundedPolygon +
// Morph port built for LoadingIndicator (see scripts/loading-indicator/) —
// Compose's CircularShapes builds this exact pair (RoundedPolygon.circle vs
// RoundedPolygon.star(innerRadius=0.75, rounding=0.35/0.4, innerRounding=0.5))
// and morphs between them by `amplitude`. The component only does a per-frame
// lerp, matching LoadingIndicator's pattern.
import { mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { writeIfChanged } from "./write-if-changed.mjs";
import {
  circlePolygon,
  cornerRounding,
  normalizePolygon,
  rotatePolygonDegrees,
  starPolygon,
} from "./loading-indicator/geometry.mjs";
import { morphMatch } from "./loading-indicator/morph.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
export const circularProgressShapesPath = join(root, "src/generated/circular-progress-shapes.ts");

// WaveSize (48dp) / ActiveThickness (4dp) / ActiveWaveWavelength (15dp) from
// CircularProgressIndicatorTokens.kt. numVertices = round(2*pi*r / wavelength),
// r = size/2 - strokeWidth/2 — CircularShapes.update()'s formula.
const SIZE = 48;
const STROKE_WIDTH = 4;
const WAVELENGTH = 15;
const RADIUS_FOR_VERTEX_COUNT = SIZE / 2 - STROKE_WIDTH / 2;
const NUM_VERTICES = Math.max(5, Math.round((2 * Math.PI * RADIUS_FOR_VERTEX_COUNT) / WAVELENGTH));

function bake(sequence) {
  const targetDiameter = SIZE - STROKE_WIDTH;
  const bakePoint = (x, y) => [
    Math.round((SIZE / 2 + (x - 0.5) * targetDiameter) * 1000) / 1000,
    Math.round((SIZE / 2 + (y - 0.5) * targetDiameter) * 1000) / 1000,
  ];
  const bakeCubic = (c) => {
    const out = Array.from({ length: 8 });
    for (let i = 0; i < 8; i += 2) [out[i], out[i + 1]] = bakePoint(c[i], c[i + 1]);
    return out;
  };
  return sequence.map(([c1, c2]) => [bakeCubic(c1), bakeCubic(c2)]);
}

export function buildCircularProgressShapes() {
  let circlePoly = normalizePolygon(circlePolygon(NUM_VERTICES, 1, 0, 0));
  let starPoly = normalizePolygon(
    starPolygon(NUM_VERTICES, 1, 0.75, cornerRounding(0.35, 0.4), cornerRounding(0.5, 0), 0, 0),
  );
  // buildRoundedPolygon's corner cubics start mid-edge, not at the vertex, so
  // the path's t=0 point sits well off angle 0 (3 o'clock). The component
  // relies on t=0 lining up with a plain SVG <circle>'s own start point (also
  // angle 0) to keep stroke-dasharray gaps symmetric, so rotate both shapes
  // by the same amount to zero that offset out (a shared rotation preserves
  // morphMatch's feature correspondence between them).
  const [cx, cy] = circlePoly.center;
  const [startX, startY] = circlePoly.features[0].cubics[0];
  const startAngleDeg = (Math.atan2(startY - cy, startX - cx) * 180) / Math.PI;
  circlePoly = rotatePolygonDegrees(circlePoly, -startAngleDeg);
  starPoly = rotatePolygonDegrees(starPoly, -startAngleDeg);
  const morph = bake(morphMatch(circlePoly, starPoly));

  const morphTs = morph.map(([c1, c2]) => `  [[${c1.join(", ")}], [${c2.join(", ")}]],`).join("\n");

  const contents = `// Generated by scripts/build-circular-progress-shapes.mjs. Do not edit by hand.
// A Morph from a plain circle (amplitude 0) to a ${NUM_VERTICES}-pointed wavy
// star (amplitude 1), as a list of [cubicStart, cubicEnd] pairs (8-number
// Bezier arrays: [a0x,a0y,c0x,c0y,c1x,c1y,a1x,a1y]) in a ${SIZE}x${SIZE}
// viewBox, matched in count/order so lerp(cubicStart, cubicEnd, amplitude) is
// valid — see LoadingIndicator's morphPathD for the same technique.
export type Cubic = readonly number[];
export type MorphPair = readonly [Cubic, Cubic];
export type Morph = readonly MorphPair[];

export const CIRCLE_STAR_MORPH: Morph = [
${morphTs}
];

export const CIRCULAR_PROGRESS_SIZE = ${SIZE};
export const CIRCULAR_PROGRESS_STROKE_WIDTH = ${STROKE_WIDTH};
export const CIRCULAR_PROGRESS_WAVELENGTH = ${WAVELENGTH};
export const CIRCULAR_PROGRESS_NUM_VERTICES = ${NUM_VERTICES};
`;

  mkdirSync(dirname(circularProgressShapesPath), { recursive: true });
  writeIfChanged(circularProgressShapesPath, contents);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  buildCircularProgressShapes();
  console.log(`Wrote ${circularProgressShapesPath}`);
}
